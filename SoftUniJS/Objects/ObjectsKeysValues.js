// Object.entries()
let cat = {
    name: 'Tommy',
    age: 5
};

console.log(Object.entries(cat));

// Object.keys()
let giraffe = {
    name: 'Nairobi',
    height: 480
};

console.log(Object.keys(giraffe));

// Object.values()
let swan = {
    name: 'Olivia',
    lake: 'Reindeer Lake'
};

console.log(Object.values(swan)); // Object Property Value